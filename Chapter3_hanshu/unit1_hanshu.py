#3.1.1  函数的概念

'''
函数是带名字的代码块，用于完成具体的任务，可以重复使用，当需要在程序中多次执行该任务的时候，
无需反复编写完成该任务的代码，只需要调用执行该函数，让python运行之中代码即可
'''


#3.1.2  函数的定义

'''
python 定义函数使用def关键字
def 函数名（阐述列表）：
    函数体
    [return]
    
1、函数代码用 def 关键字开头，后接函数标识名称和括号（）
2、函数第一行语句可以选择性使用文档字符串用于函数说明，单引号，双引号、三引号都行
3、return 结束函数，选择性返回一个值给调用方，可以不带
3、函数名称一般是一个动词，第一个单词小谢，其他每个单词首字母大写

'''








